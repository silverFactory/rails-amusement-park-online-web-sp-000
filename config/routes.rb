  root 'sessions#index'
  get '/signin' => 'sessions#new'
  post '/signin' => 'sessions#create'
  get '/signout' => 'sessions#destroy'
  post '/ride' => 'attractions#ride'
  # For details on the DSL available within this file, see http://guides.rubyonrails.org/routing.html
end
